# Demonstration of various data types in Python

# Numeric data type (integer and float)
integer_value = 10
float_value = 3.14
print(f"Data type of integer_value: {type(integer_value)}")
print(f"Data type of float_value: {type(float_value)}")

# Sequence data type (string, list, tuple)
string_value = "Hello, World!"
print(f"Data type of string_value: {type(string_value)}")

list_value = [1, 2, 3, 4, 5]
print(f"Data type of list_value: {type(list_value)}")

tuple_value = (10, 20, 30, 40, 50)
print(f"Data type of tuple_value: {type(tuple_value)}")

# Boolean data type
boolean_value = True
print(f"Data type of boolean_value: {type(boolean_value)}")

# Set data type
set_value = {1, 2, 3, 4, 5}
print(f"Data type of set_value: {type(set_value)}")

# Dictionary data type
dict_value = {'key': 'value', 'another_key': 123}
print(f"Data type of dict_value: {type(dict_value)}")
