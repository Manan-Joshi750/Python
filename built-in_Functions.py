# Demonstration of built-in functions in Python

# Using type() function to check the data type of a variable
number = 10
print(f"Data type of 'number': {type(number)}")

name = "John"
print(f"Data type of 'name': {type(name)}")

# Using print() function to display output
print("Hello, this is a demonstration of built-in functions!")

# Using input() function to take user input
user_input = input("Enter your name: ")
print(f"Hello, {user_input}!")

# Using range() function to create a range of numbers
for i in range(1, 6):
    print(f"Number: {i}")
